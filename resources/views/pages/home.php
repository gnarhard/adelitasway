                    <h2 class="mb-0">THE BAND</h2>
                    <h4>BIO</h4>
                    <p class="text-light">Adelitas Way formed in 2006 and quickly created a massive buzz in their hometown Las Vegas music scene, by consistently selling out shows left and right. Fronted by lead singer/songwriter Rick Dejesus, and joined by drummer Trevor Safford and bassist Andrew Cushing, they signed to Virgin Records and broke into the mainstream with their anthemic song Invincible from their self-titled debut album.</p>
                    <p class="text-light">Invincible was a top 5 single at Active Rock Radio and paved the way for them with numerous television spots, including the CSI Miami finale, it served as the theme song for the weekly WWE Superstars show, was featured in the THQ video game Raw vs Smackdown, and also used on Fox Sports Network as the college football theme song. Additionally, it was featured on ESPN Monday night football, and on Fox (MLB playoffs), and the track went on to become Certified Gold.</p>
                    <div id="moreText">
                        <p class="text-light">The band’s follow up sophomore effort, Home School Valedictorian, arrived in Summer 2011, spawning four Top 5 Active Rock Radio singles (Sick, The Collapse, Criticize, and Alive, two of which went to #1 (Alive and Criticize). The band performed Alive on ABC's The Bachelor Pad on national television. This album cycle marked the band going on to do the Carnival Of Madness tour in 2011 with the likes of Theory Of A Deadman, Alter Bridge and Black Stone Cherry, as well as being on the mainstage on Uproar Fest 2012 with their peers in Shinedown, Godsmack, Papa Roach and Staind. The record marks the band’s highest selling album to date, having sold over 250,000 copies, over 1.5 Million iTunes singles, and has gathered over 150 million streams across DSPS.</p>
                        <p class="text-light">Adelitas Way third album Stuck was released in Summer 2014, and had a Top 5 radio single with Dog on a Leash. The track We Came was later used as ESPN Monday Night Football theme song. After the Stuck album cycle, the band’s next record Getaway showed the band leaving Virgin Records and becoming fully independent. The band released the hit single Bad Reputation and the track went to #6 at the Active Rock Radio charts. Adelitas Way then embarked on their biggest headlining tour to date in support of the release; selling out over half the venues on a 40+ date trek across the United States. The band’s single Ready for War (Pray For Peace) released on Sept 30th 2017, and reached #18 at Active Rock Radio fully independently, and was also used as the official theme song of WWE PPV TLC. Notorious was released in 2017 and became one of the band's most successful singles, gaining over 75 million streams, and becoming the first independent Certified Gold record for the band.</p>
                        <p class="text-light">Today, the band’s presence over the last two decades in the rock scene has certainly been felt as a force to be reckoned with - they have become proven industry veterans, with 8 full length albums under their belt, culminating in over 1 billion combined streams across their catalog. Throughout their career, the band has relentlessly toured and built up a strong live following, touring with the likes of Guns N Roses, Shinedown, Creed, Skillet, Three Days Grace, Halestorm, Breaking Benjamin, Staind, Alter Bridge, Theory of a Deadman, Skillet, 10 Years, Buckcherry, Hinder, Red, Flyleaf, The Pretty Reckless and more, as well as successfully headlining sold out venues throughout the country. They have played the mainstage at the biggest Rock festivals in America, such as Rock On The Range, Carolina Rebellion, Louder Than Life, Welcome To Rockville, Blue Ridge Rock Festival, RockFest in Cadott, WI, Rock USA, Sturgis Bike Rally, and many more. One thing is for certain - Adelitas Way is a massive independently successful rock act, and here to stay and pave the way for the future of rock and roll.</p>
